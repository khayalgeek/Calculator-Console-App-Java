public class Util {
    public static void Addition(int a, int b){
        int sum = a+b;
        System.out.println("Result ="+sum);
    }

    public static void Subtraction(int a, int b){
        int sum = a-b;
        System.out.println("Result ="+sum);
    }
    public static void Multiplication(int a, int b){
        int sum = a*b;
        System.out.println("Result ="+sum);
    }
    public static void Division(int a, int b){
        int sum = a/b;
        System.out.println("Result ="+sum);
    }
}
